						　　都美竹：压力大，但压力大的事情终究要有人去做，我不是什么伟大的人，最开始也就是一时气愤，但是后来发现找到我的这些姐妹，她们的情况很令人震惊，不得不承认，我现在就是想戳破吴的面具，不让任何人再受到伤害，我希望我是最后一个受害者。
					</p>
					<p>　　</p>
					<center>
						<img
							alt="\"
							src="https://nimg.ws.126.net/?url=https%3A%2F%2Fnimg.ws.126.net%2F%3Furl%3Dhttp%253A%252F%252Fdingyue.ws.126.net%252F2021%252F0718%252Fc2700e27j00qwf9s2000pc000hl00bcc.jpg%26thumbnail%3D660x2147483647%26quality%3D80%26type%3Djpg&amp;thumbnail=690x2147483647&amp;quality=75&amp;type=jpg"
