					<p>
						　　都美竹：我当时大脑一片发蒙，在床上盯着天花板呆了好一会，才敢相信。
					</p>
					<p>
						　　吴在我旁边，还没醒，我当时都不会生气了，只是不知道该怎么办，吴从来不戴套，不光是我，和别人也一样。我当时立刻想穿衣服跑，但没想到一出门，他表哥在客厅睡觉，他表哥一直没走，那是我和他在一起唯一一次他表哥一宿没走的情况，我后来想过，那是他害怕我醒来去报警。我走回卧室，站在床旁，站了好一会他才醒，他醒来就问我干嘛站着，又拉着我躺在床上，我当时都没有思考能力了，他说他会对我负责，照顾我一辈子，过年带我去见他妈，而且他表情很真挚，和他电影里的不同，在生活中，最起码那一天他是影帝。
					</p>
					<p>　　网易娱乐：你们联系频繁吗?</p>
					<p>　　都美竹：他闲下来回北京才会找我。</p>
					<p>　　网易娱乐：第一次收手机是什么时候?会不会觉得不被信任?</p>
					<p>
						　　都美竹：第一次收手机是第一次喝酒的时候，我心里想毕竟他是明星吧，后来去卧室不让带手机，我也是这么想的，明星就是高人一等，特殊对待吧，其实这段时间我反思过很多，我觉得我也被饭圈文化洗脑了，如果他真的爱我，不可能收我的手机。
					</p>
					<p>　　网易娱乐：在一起的时间怎么度过?</p>
					<p>
						　　都美竹：看电视，看电影，一般不出去，在家里呆着，聊天，听他吹牛逼。
					</p>
					<p>　　网易娱乐：会介绍给他的朋友给你吗，进入他的圈子?</p>
					<p>
						　　都美竹：除了他工作人员，保姆，表哥，还有某些我们在一起时候正好来他家的制作人什么的，别的没介绍过。他说要给我资源，他和别的被骗姐妹也说过，但统统没有实现。
					</p>
					<p>　　网易娱乐：什么时候态度发生转变的?</p>
					<p>
						　　都美竹：今年三月份，我发消息不回了，不经常打电话了，不每天早安晚安了，我发消息过去他要么不回，要么很久才回一个表情。
					</p>
					<p>　　网易娱乐：你认为你们是恋爱关系吗?</p>
					<p>　　都美竹：我自认为是吧，我只能这么认为了。</p>
					<p>　　网易娱乐：现在你了解吴亦凡吗?</p>
					<p>
						　　都美竹：我应该算是了解吧，即便到今天为止，我都觉得他很傻，他甚至有的时候傻得可爱，他是一个妈宝男，那种感觉你懂吧。
					</p>
					<p>　　网易娱乐：跟你之前认识的吴亦凡相比，有什么不同?</p>
					<p>
						　　都美竹：大众面前的吴貌似很高冷，但我面前很中二，甚至情商很低。
					</p>
					<p>　　网易娱乐：曾经想过和明星谈恋爱吗?</p>
					<p>
						　　都美竹：的确有，但具体是谁就不说了，但这次之后不会了，明星台上台下差别很大，尤其是男明星，他们吃的女人饭，很多时候，我感觉到假。
					</p>
					<p>　　网易娱乐：和明星恋爱跟普通人谈恋爱的不同是什么?</p>
					<p>
						　　都美竹：不同就在于一个在微博经常上热搜，然后在电视上能看到的人在现实中也能看到吧，看到他那么多粉丝，而你误认为那么多女孩喜欢他，可是他只喜欢你，可能内心会有虚荣吧，当然，现在看，这些都是泡影。
					</p>
					<p>　　网易娱乐：有没有人提醒你，跟明星谈恋爱的风险?</p>
					<p>
						　　都美竹：会有，没人提醒自己也知道，毕竟那么多人曝光过他，但他本人的确显得很老实，甚至有点笨拙，二了吧唧那种感觉，他说那些曝光他的人是想蹭他热度，可能我第一次深度恋爱吧，就信了，呵呵，反讽。
					</p>
					<p>　　网易娱乐：跟吴亦凡谈恋爱，你最大的感受是什么?</p>
					<p>　　都美竹：最大的感受就是以后再也不想谈恋爱了。</p>
